# in python the OOP have following four principles
# 1. Encapsulation ( Encapsulating data and methods within a class . It helps in bundling the data and methods together as a single entity)
# 2. Abstraction ( Hiding complex implementation details and exposing only the necessary parts )
# 3. Inheritance ( Creating new classes based on existing ones to promote code reuse )
# 4. Polymorphism ( Ability to use a single interface to represent different underlying forms (data types) )
